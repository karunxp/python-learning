######
#Escape sequence        Meaning
# \,                    single quote
# \"                    double quote
# \\                    backslash
# \n                    new line
# \t                    tab
# \b                    backspace

print("Hello \"World\" world")
print('I \'m Ashrit')

print('line A')
print('line B')
print('line A \nline B')

print('name \t Arun')

print('this is backslash\\')

print("this is double backslash\\\\")

