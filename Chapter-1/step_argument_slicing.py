# slicing / slecting sub sequences

#lang = "Python"
# syntax - [ start argument : stop argument -1 : step]

print("ArunKumar"[0:5])

print("ArunKumar"[0:5:2]) # step argument
print("ArunKumar"[0::2]) # step argument

print("ArunKumar"[5::-1]) # step argument

print("ArunKumar"[-1::-1]) # reverse string
print("ArunKumar"[::-1]) # reverse string